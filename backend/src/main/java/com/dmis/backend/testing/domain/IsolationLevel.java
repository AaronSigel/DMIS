package com.dmis.backend.testing.domain;

/**
 * Уровень изоляции данных для тестовой сессии.
 */
public enum IsolationLevel {
    /**
     * Все операции в одной транзакции с rollback в конце.
     * Быстро, но не поддерживает параллельные прогоны.
     */
    TRANSACTION,

    /**
     * Отдельная БД-схема для каждой сессии.
     * Поддерживает параллельные прогоны, требует больше ресурсов.
     */
    SCHEMA,

    /**
     * Отдельная in-memory БД для каждой сессии.
     * Полная изоляция, но медленнее из-за миграций.
     */
    DATABASE,

    /**
     * Без изоляции — работа с реальными данными.
     * Только для smoke-тестов в demo-окружении.
     */
    NONE
}
