package com.dmis.backend.testing.domain;

/**
 * Политика очистки данных после завершения тестовой сессии.
 */
public enum CleanupPolicy {
    /**
     * Всегда очищать данные после теста.
     */
    ALWAYS,

    /**
     * Очищать только при успешном прохождении теста.
     */
    ON_SUCCESS,

    /**
     * Очищать только при провале теста.
     */
    ON_FAILURE,

    /**
     * Никогда не очищать — оставлять данные для отладки.
     */
    NEVER
}
